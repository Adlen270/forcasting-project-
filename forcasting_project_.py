def smape(y_true, y_pred):
    y_true = np.array(y_true)
    y_pred = np.array(y_pred)

    denominator = (np.abs(y_true) + np.abs(y_pred)) / 2
    denominator = np.where(denominator == 0, 1e-8, denominator)

    return np.mean(np.abs(y_true - y_pred) / denominator) * 100


def evaluate_model(y_true, y_pred):
    mae = mean_absolute_error(y_true, y_pred)
    rmse = np.sqrt(mean_squared_error(y_true, y_pred))
    s_mape = smape(y_true, y_pred)
    r2 = r2_score(y_true, y_pred)

    return {
        "MAE": mae,
        "RMSE": rmse,
        "sMAPE": s_mape,
        "R2": r2
    }

class BiLSTMGlobalPoolingEmbedder(nn.Module):
    def __init__(self, n_features, hidden_size=64, num_layers=1, dropout=0.1):
        super().__init__()

        self.lstm = nn.LSTM(
            input_size=n_features,
            hidden_size=hidden_size,
            num_layers=num_layers,
            batch_first=True,
            bidirectional=True,
            dropout=0 if num_layers == 1 else dropout
        )

        lstm_output_dim = hidden_size * 2
        embedding_dim = lstm_output_dim * 3

        self.head = nn.Sequential(
            nn.Linear(embedding_dim, 128),
            nn.ReLU(),
            nn.Dropout(dropout),

            nn.Linear(128, 64),
            nn.ReLU(),
            nn.Dropout(dropout),

            nn.Linear(64, 1)
        )

    def forward(self, x, return_embedding=False):
        output, (h, c) = self.lstm(x)

        mean_pool = output.mean(dim=1)
        max_pool = output.max(dim=1).values
        min_pool = output.min(dim=1).values

        embedding = torch.cat(
            [mean_pool, max_pool, min_pool],
            dim=1
        )

        pred = self.head(embedding).squeeze(-1)

        if return_embedding:
            return pred, embedding

        return pred

def train_bilstm_for_embeddings(
    X_train_seq,
    y_train,
    X_val_seq,
    y_val,
    hidden_size=64,
    epochs=60,
    lr=1e-3,
    batch_size=128,
    patience=8
):
    y_scaler = StandardScaler()

    y_train_scaled = y_scaler.fit_transform(
        np.array(y_train).reshape(-1, 1)
    ).ravel()

    y_val_scaled = y_scaler.transform(
        np.array(y_val).reshape(-1, 1)
    ).ravel()

    train_ds = TensorDataset(
        torch.tensor(X_train_seq, dtype=torch.float32),
        torch.tensor(y_train_scaled, dtype=torch.float32)
    )

    val_ds = TensorDataset(
        torch.tensor(X_val_seq, dtype=torch.float32),
        torch.tensor(y_val_scaled, dtype=torch.float32)
    )

    train_loader = DataLoader(
        train_ds,
        batch_size=batch_size,
        shuffle=True
    )

    val_loader = DataLoader(
        val_ds,
        batch_size=batch_size,
        shuffle=False
    )

    model = BiLSTMGlobalPoolingEmbedder(
        n_features=X_train_seq.shape[2],
        hidden_size=hidden_size,
        num_layers=1,
        dropout=0.1
    ).to(device)

    optimizer = torch.optim.AdamW(
        model.parameters(),
        lr=lr,
        weight_decay=1e-4
    )

    loss_fn = nn.MSELoss()

    best_val_loss = np.inf
    best_state = None
    patience_counter = 0

    for epoch in range(epochs):
        model.train()
        train_losses = []

        for xb, yb in train_loader:
            xb = xb.to(device)
            yb = yb.to(device)

            optimizer.zero_grad()
            pred = model(xb)
            loss = loss_fn(pred, yb)
            loss.backward()
            optimizer.step()

            train_losses.append(loss.item())

        model.eval()
        val_losses = []

        with torch.no_grad():
            for xb, yb in val_loader:
                xb = xb.to(device)
                yb = yb.to(device)

                pred = model(xb)
                loss = loss_fn(pred, yb)
                val_losses.append(loss.item())

        train_loss = np.mean(train_losses)
        val_loss = np.mean(val_losses)

        if epoch % 5 == 0:
            print(f"Epoch {epoch} | Train loss: {train_loss:.4f} | Val loss: {val_loss:.4f}")

        if val_loss < best_val_loss:
            best_val_loss = val_loss
            best_state = copy.deepcopy(model.state_dict())
            patience_counter = 0
        else:
            patience_counter += 1

        if patience_counter >= patience:
            print("Early stopping.")
            break

    model.load_state_dict(best_state)

    return model

def extract_bilstm_embeddings(model, X_seq, batch_size=256):
    model.eval()

    loader = DataLoader(
        torch.tensor(X_seq, dtype=torch.float32),
        batch_size=batch_size,
        shuffle=False
    )

    embeddings = []

    with torch.no_grad():
        for xb in loader:
            xb = xb.to(device)
            _, emb = model(xb, return_embedding=True)
            embeddings.append(emb.cpu().numpy())

    return np.vstack(embeddings)

