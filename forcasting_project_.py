class Chomp1d(nn.Module):
    """
    Removes extra timesteps created by padding.
    This keeps the convolution causal.
    """
    def __init__(self, chomp_size):
        super().__init__()
        self.chomp_size = chomp_size

    def forward(self, x):
        if self.chomp_size == 0:
            return x
        return x[:, :, :-self.chomp_size].contiguous()


class TemporalBlock(nn.Module):
    """
    One residual TCN block:
    dilated causal convolution + ReLU + dropout + residual connection.
    """
    def __init__(
        self,
        in_channels,
        out_channels,
        kernel_size,
        dilation,
        dropout
    ):
        super().__init__()

        padding = (kernel_size - 1) * dilation

        self.conv1 = nn.Conv1d(
            in_channels,
            out_channels,
            kernel_size,
            padding=padding,
            dilation=dilation
        )
        self.chomp1 = Chomp1d(padding)
        self.relu1 = nn.ReLU()
        self.dropout1 = nn.Dropout(dropout)

        self.conv2 = nn.Conv1d(
            out_channels,
            out_channels,
            kernel_size,
            padding=padding,
            dilation=dilation
        )
        self.chomp2 = Chomp1d(padding)
        self.relu2 = nn.ReLU()
        self.dropout2 = nn.Dropout(dropout)

        self.downsample = (
            nn.Conv1d(in_channels, out_channels, kernel_size=1)
            if in_channels != out_channels
            else None
        )

        self.final_relu = nn.ReLU()

    def forward(self, x):
        out = self.conv1(x)
        out = self.chomp1(out)
        out = self.relu1(out)
        out = self.dropout1(out)

        out = self.conv2(out)
        out = self.chomp2(out)
        out = self.relu2(out)
        out = self.dropout2(out)

        residual = x if self.downsample is None else self.downsample(x)

        return self.final_relu(out + residual)


class TCNRegressor(nn.Module):
    """
    TCN model that returns:
    - prediction
    - temporal embedding if return_embedding=True

    Input shape:
    batch_size × seq_len × n_features

    Conv1D expects:
    batch_size × n_features × seq_len
    """
    def __init__(
        self,
        n_features,
        channels=[64, 64, 128],
        kernel_size=3,
        dropout=0.1
    ):
        super().__init__()

        layers = []
        in_channels = n_features

        for i, out_channels in enumerate(channels):
            dilation = 2 ** i

            layers.append(
                TemporalBlock(
                    in_channels=in_channels,
                    out_channels=out_channels,
                    kernel_size=kernel_size,
                    dilation=dilation,
                    dropout=dropout
                )
            )

            in_channels = out_channels

        self.tcn = nn.Sequential(*layers)

        final_channels = channels[-1]

        # Global pooling: remembers entire 48h window
        embedding_dim = final_channels * 3  # mean + max + last

        self.head = nn.Sequential(
            nn.Linear(embedding_dim, 128),
            nn.ReLU(),
            nn.Dropout(dropout),

            nn.Linear(128, 64),
            nn.ReLU(),
            nn.Dropout(dropout),

            nn.Linear(64, 1)
        )

    def forward(self, x, return_embedding=False):
        # x: batch × seq_len × features
        x = x.transpose(1, 2)  # batch × features × seq_len

        out = self.tcn(x)      # batch × channels × seq_len

        mean_pool = out.mean(dim=2)
        max_pool = out.max(dim=2).values
        last_state = out[:, :, -1]

        embedding = torch.cat(
            [mean_pool, max_pool, last_state],
            dim=1
        )

        pred = self.head(embedding).squeeze(-1)

        if return_embedding:
            return pred, embedding

        return pred


