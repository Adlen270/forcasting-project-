def extract_tcn_embeddings(model, Xseq, batch_size=256):
    model.eval()

    loader = DataLoader(
        torch.tensor(Xseq, dtype=torch.float32),
        batch_size=batch_size,
        shuffle=False
    )

    embeddings = []

    with torch.no_grad():
        for xb in loader:
            xb = xb.to(device)

            _, emb = model(
                xb,
                return_embedding=True
            )

            embeddings.append(emb.cpu().numpy())

    embeddings = np.vstack(embeddings)

    return embeddings


